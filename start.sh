#!/usr/bin/env bash

# الأمر الأول: تهيئة قاعدة البيانات (طريقة موثوقة للتشغيل على Render)
# يقوم بتشغيل app.py بشكل مؤقت لإنشاء قاعدة البيانات إذا لم تكن موجودة.
python app.py & PID=$!
sleep 5 

# قتل عملية التشغيل المؤقتة
kill $PID

# الأمر الثاني: تشغيل خادم Gunicorn لتشغيل التطبيق بشكل دائم
gunicorn app:app